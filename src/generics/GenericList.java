package generics;

/**
 *
 * @author huynq
 * @param <T>
 */
public class GenericList<T> {

//    private T[] items = new T[10]; // will not compile
    private final T[] items = (T[]) new Object[10];
    private int count;

    public void add(T item) {
        items[count++] = item;
    }

    public T get(int index) {
        return items[index];
    }

}
